package day08ternary_switch;

import java.util.Locale;
import java.util.Scanner;

public class Switch04 {

    public static void main(String[] args) {

        char m ='A';
        switch(m) {
            case 'A':
                System.out.println("First letter");

            case 'B':
                System.out.println("Second letter");

            case 'C':
                System.out.println("Third letter");
                break;

            case 'D':
                System.out.println("Fourth letter");
            default:
                System.out.println("What kind of letter this? ");  //What kind of letter this?


        }




    }







}